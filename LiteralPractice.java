public class LiteralPractice{
    public static void main(String[] args){
        
        long greatNum = 300100000L;
        System.out.println(greatNum);
        
        float crazyFloat = 3.14f;
        System.out.println(crazyFloat);
        
        char single = '\u2764';
        System.out.println(single);
    }
}