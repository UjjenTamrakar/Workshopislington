public class DataTypeInspector{
    public static void main(String[] args){
         
        byte youbyte = 10;
        System.out.println(youbyte);
        
        short youShort = 200;
        System.out.println(youShort);
        
        int youInt = 1000;
        System.out.println(youInt);
        
        long youLong = 10000;
        System.out.println(youLong);
        
        float youFloat = 3.14f;
        System.out.println(youFloat);
        
        double youDouble = 120.12313;
        System.out.println(youDouble);
        
        char youChar = 'U';
        System.out.println(youChar);
        
        boolean youBoolean = false;
        System.out.println(youBoolean);
    }
}